#include "libft.h"

void	*ft_memchr(const void *s, int c, size_t n)
{
	int i;
	i = 0;
	while (n-- && s[i])
	{
		if (s[i] == c)
			return (&s[i]);
		i++;
	}
	return (NULL);
}
