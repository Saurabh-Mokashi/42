#include <stdio.h>

char *ft_strrchr(const char *s, int c)
{
        int i=ft_strlen(s);
        char *ptr;
        ptr=s[i];
        while(i>=0)
        {
                if(s[i]==c)
                {
                        return ptr;
                }
                i--;
                ptr--;
        }
        return NULL;
}

