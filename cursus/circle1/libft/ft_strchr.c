#include "libft.h"

char	*ft_strchr(const char *s, int c)
{
	int i;
	char *ptr;

	i = 0;
	ptr = s;
	while (s[i])
	{
		if (s[i] == c)
		{
			return (ptr);
		}
		i++;
		ptr++;
	}
/*	if (c==0)
		return s;
*/
	return (NULL);
}
