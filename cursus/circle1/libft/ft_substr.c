#include "libft.h"

char	*ft_substr(char const *s, unsigned int start, size_t len)
{
	char *str;
	int i;
	
	if (!s)
		return (NULL);
	if (start > ft_strlen(s))
		return ("");
	if(len > ft_strlen(s) - start)
		len = ft_strlen(s) - start;
	str = malloc(len + 1);
	if (!str)
		return (NULL);
	i = 0;
	while (i < len)
	{
		str[i] = s[start+i];
		i++;
	}
	str[i] = '\0';
	return (str);
}
